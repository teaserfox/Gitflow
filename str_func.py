def str_upper():
    '''
    Функция букв верхнего регистра
    '''
    line_1 = "asdfghjkl"
    return line_1.upper()


print(str_upper())


def str_title():
    '''
    Функция заглавных букв каждого слова
    '''
    Line_1 = "пусть бегут неуклюже, пешеходы по лужам"
    return Line_1.title()


print(str_title())
