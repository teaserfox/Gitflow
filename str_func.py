def str_upper():
    '''
    Функция символов верхнего регистра текста
    '''
    line_1 = "asdfghjkl"
    return line_1.upper()


print(str_upper())


def str_title():
    '''
    Функция заглавных символов каждого слова в тексте
    '''
    Line_1 = "пусть бегут неуклюже, пешеходы по лужам"
    return Line_1.title()


print(str_title())
